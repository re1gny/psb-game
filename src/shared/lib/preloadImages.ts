export const preloadAllImages = async () => {
  const preloadImage = (src: string) => {
    return new Promise((resolve, reject) => {
      const img = new Image()
      img.onload = function() {
        resolve(img)
      }
      img.onerror = img.onabort = function() {
        reject(src)
      }
      img.src = src
    })
  }

  try {
    await Promise.all(ALL_IMAGES.map(preloadImage))
  } catch {
    console.error('Error with preloading images')
  }
}